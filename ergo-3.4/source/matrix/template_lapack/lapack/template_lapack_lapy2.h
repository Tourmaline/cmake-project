#ifndef TEMPLATE_LAPACK_LAPY2_HEADER
#define TEMPLATE_LAPACK_LAPY2_HEADER


template<class Treal>
Treal template_lapack_lapy2(Treal *x, Treal *y)
{
/*  -- LAPACK auxiliary routine (version 3.0) --   
       Univ. of Tennessee, Univ. of California Berkeley, NAG Ltd.,   
       Courant Institute, Argonne National Lab, and Rice University   
       October 31, 1992   


    Purpose   
    =======   

    DLAPY2 returns sqrt(x**2+y**2), taking care not to cause unnecessary   
    overflow.   

    Arguments   
    =========   

    X       (input) DOUBLE PRECISION   
    Y       (input) DOUBLE PRECISION   
            X and Y specify the values x and y.   

    ===================================================================== */
    /* System generated locals */
    Treal ret_val, d__1;
    /* Local variables */
     Treal xabs, yabs, w, z__;



    xabs = absMACRO(*x);
    yabs = absMACRO(*y);
    w = maxMACRO(xabs,yabs);
    z__ = minMACRO(xabs,yabs);
    if (z__ == 0.) {
	ret_val = w;
    } else {
/* Computing 2nd power */
	d__1 = z__ / w;
	ret_val = w * template_blas_sqrt(d__1 * d__1 + 1.);
    }
    return ret_val;

/*     End of DLAPY2 */

} /* dlapy2_ */

#endif
